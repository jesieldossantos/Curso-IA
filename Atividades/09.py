#  Crie um algoritmo que verifique se um número inserido pelo usuário é par ou ímpar.

numero = int(input("Digite um número: "))

if numero % 2 == 0:
    print(f"O número {numero} é par.")
else:
    print(f"O número {numero} é ímpar.")

