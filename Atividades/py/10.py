#  Crie um algoritmo que solicite ao usuário uma idade e verifique se ela é maior ou igual a 18.

idade = int(input("Digite sua idade: "))

if idade >= 18:
    print("Você é maior de idade.")
else:
    print("Você é menor de idade.")

